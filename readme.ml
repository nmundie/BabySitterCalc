To run the BabySitter Day Wage Calculation:

1) Install Maven from https://maven.apache.org
2) To build and Run Junit tests, execute 'mvn clean install'
3) To execute the main method in the BabySitterHourlyEmployee class, use command mvn exec:java -Dexec.mainClass="com.oh.babysitter.wagecalc.BabySitterHourlyEmployee"